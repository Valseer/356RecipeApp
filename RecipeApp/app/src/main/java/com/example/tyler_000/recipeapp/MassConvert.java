public class MassConvert {
    //pound(lb)  1 lb = 16 oz
    //ounce(oz)

/**
 * POUND
 */

    /**
     * pound to Ounces
     **/
    public double pound_to_Ounces(double pound )
    {
        return (pound * 16);
    }

/**
 * OUNCES
 */
    /**
     * ounce (oz) to pound
     **/
    public double Ounce_to_pound(double Ounce )
    {
        return (Ounce * 0.0625);
    }
}
